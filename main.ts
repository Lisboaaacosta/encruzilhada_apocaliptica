// Logic of Walking Player
forever(function () {
    if (controller.left.isPressed()) {
        sentido_x = -1
        Prota.x += -2
    }
    if (controller.right.isPressed()) {
        sentido_x = 1
        Prota.x += 2
    }
    if (controller.up.isPressed()) {
        if (Prota.y >= 20) {
            sentido_y = -1
            Prota.y += -2
        }
    }
    if (controller.down.isPressed()) {
        if (Prota.y <= scene.screenHeight() - 10) {
            sentido_y = 1
            Prota.y += 2
        }
    }
    scene.centerCameraAt(Prota.x, 60)
    Abilities()
})
